      <div class="interview__cont">
        <h3 class="interview__ttl">人を育て、組織を育てる</h3>
        <p class="interview__txt interview__txt--green">— 社長就任から7年経った現在。砂押社長は組織の在り方を少しずつ変えていった。最初の2年は守りの姿勢だったが、次第に自分なりの経営スタイルを模索し始める。3人だった社員は現在11人に増え、デジタル化や業務改善も進めてきた。</p>
        <p class="interview__txt">社員とのコミュニケーションひとつとっても、従業員時代の10年は冗談ひとつ言わず、プライベートな話も一切しませんでした。週末にどこに行くとか何をするとか、そんな会話の必要性が正直分からなかったんです。でも、今の明山茶業の規模や雰囲気を考えると、私が何者なのかを積極的に出した方がいいと思うようになって。</p>
        <p class="interview__txt interview__txt--green">— その変化は、予想以上の恩恵をもたらした。社員との会話が増えれば、それに伴って仕事に関する意見や提案も活発に上がり始める。長年の経験を持つ社員たちが、内に秘めていたアイデアを次々と形にしていく契機になった。</p>
        <p class="interview__txt">スタッフの多くは長年会社に在籍していて、私の幼少期から関わってきた人たちでもあります。長くてもう20年から30年。これまで考えてることもたくさんあったんだと思います。私が経営を引き継いだ当初、率直な意見をいろいろ投げてくれて、不安定な時期もすごく助けられました。</p>
        <p class="interview__txt interview__txt--green">— 組織の改革は、デジタル化という形でも進められた。紙の台帳からパソコンへの移行、クラウドを活用した受発注システムの導入。昭和の時代から続いてきた業務フローを、一つひとつ見直していった。</p>
        <p class="interview__txt">既存のお客様からもデジタル化への順応を求められることが多くなってきたんです。特定のシステムを使わないと受発注ができないとか。紙ではなくデータでのやり取りが必要になったりとか。少し前までは、とにかくがむしゃらに作っては売ってを繰り返す商売だったので、そういう抜本的な、仕組みごと変える余裕はなかったんです。でも少しずつ、いろいろな課題に取り組める余裕がでてきて。これまでは手作業でやってきたブレンドの配合もきちんと数値化したり、役割を分担して外注に依頼したりとか。そうやって、会社を継いでからの7年は、先代が築いた関係や信頼を守りつつ、少しずつ周りの環境に適応するために形を変えていく期間でもありました。</p>
      </div>

