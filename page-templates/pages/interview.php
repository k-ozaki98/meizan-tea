      <div class="interview__cont">
        <p class="interview__txt">1981年（昭和56年）、バブル経済の幕開けとともに誕生した明山茶業。創業者の砂押雅夫が、「日本を健康に」という想いを胸に抱き、台湾茶の輸入販売という事業を開始する。当時はまだペットボトルも缶飲料も少なく、茶を淹れることが当たり前だった。それから40年余り。時代とともに業界も変化を遂げる中、明山茶業は先代が築いた基盤を土台に、現代に即した企業へと深化を遂げている。</p>
      </div>

      <div class="interview__cont">
        <h3 class="interview__ttl">受け継いだもの</h3>
        <p class="interview__txt">私と会社は同い年なんです。父が創業したのは、私が生まれる1ヶ月前のこと。母も一緒に立ち上げに関わっていたので、まさに家族とともに歩み始めた会社でした。当時は健康ブームの走りで、父は「日本を健康に」という強い想いがあったようです。特にコレステロール値を下げる効果のある烏龍茶に着目して。</p>
        <p class="interview__txt interview__txt--green">— 創業当時、お茶を原料ベースで輸入し、日本で販売する企業は稀少だった。その中で明山茶業は、台湾茶の輸入販売における先駆者として歩みを始める。</p>
        <p class="interview__txt">子供の頃から、家でもずっとお茶の話ばかり。夜遅くまで仕事をする父の姿も当たり前で、お茶は日常そのもの、そんな環境でした。父が事務所から家にお茶を持ち帰ってくると、私も梱包作業を手伝ったりしていました。創業時から在籍している社員は今でも働いてくれていて、私が6、7歳の頃からその姿を見ています。</p>
        <p class="interview__txt interview__txt--green">— 幼い頃から会社の空気を知る砂押社長だったが、事業承継の具体的な話し合いはなかった。取締役として名を連ねてはいたものの、父との約束も、明確な引継ぎ計画もないままに時は流れていく。そんな中、〇〇年に父の急な入院により、その翌月には代表を引き継ぐことになる。</p>
        <p class="interview__txt">最初の不安は、社員がどう思うかというところでした。場合によっては「じゃあ辞めます」という話にだってなるかもしれない。それぐらい創業社長とその娘とでは、存在の大きさがまるで違うものだと思っていて。41年やってきた会社の看板と、築いてきた関係性を引き継ぐというのは、本当に大変な仕事だと思いました。</p>
        <p class="interview__txt interview__txt--green">— その不安を抱えながら、各所に挨拶回りに奔走する毎日が始まる。病に伏した父はもちろん同行してくれず、一人で取引先を回ることになる。その中である出会いが、経営者としての重要な転機となった。</p>
        <p class="interview__txt">餃子の王将の渡邊社長に挨拶にうかがった時のことです。数々の助言をいただいた最後に「一番大事なことは何だと思う？」と聞かれて。壁に掛かっていた「愛」という書をちらっと盗み見てとっさに「愛」と答えたら、「それだ！」と（笑）。カンニングこそしましたが、実際にその言葉を口にしたことで、何かが腑に落ちた感覚があって。それまでは会社が潰れないように、誰にも迷惑をかけないように、というディフェンシブな考え方をしていました。でも、お茶に対しても、社員に対しても、会社に対しても、そういうこちらから発露する気持ちがないと、苦労も楽しいことも続いていかないんだと。照れ臭さの奥にあった本質に気づいたような感覚があったんです。</p>
      </div>

