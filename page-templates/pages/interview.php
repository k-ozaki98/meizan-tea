      <div class="interview__cont">
        <h3 class="interview__ttl">戦略なき戦略と、会社の理想像</h3>
        <p class="interview__txt interview__txt--green">— 二代目として社長に就任して以来、外部からの要望に応える形でデジタル化や組織改革を進めてきた。しかし、それ以上に重視してきたのは、社員と向き合い、その可能性を引き出すこと。その背景には、社長自身の深い内省がある。</p>
        <p class="interview__txt">私自身、子供の頃から自信がなくて、自分には何も得意なものがないと思っていました。だからこそ、常に人の気持ちを観察し、相手が何を望んでいるのか考える癖があって。当時は、認めてもらいたいという気持ちが強かったんだと思います。でもそれが今となっては、社員一人一人の個性や得意分野を見出すことに繋がったのかもしれません。</p>
        <p class="interview__txt interview__txt--green">— そうした内側への視点は、結果として売上の向上という成果にもつながった。それは戦略として計画されたものではなく、社長自身の個性が生んだ、数字では測れない血の通ったプロセスから。</p>
        <p class="interview__txt">社員がそれぞれの得意分野を活かして動いてくれる、そういう仕組みが少しずつ上手く回り始めた。今まで外側に向けてというより、とにかく内側の基盤を作ることに注力してきた。少し遠回りしたかもしれないけど、それは間違いではなかったと今では思います。</p>
        <p class="interview__txt interview__txt--green">— 組織の在り方を模索する中で、砂押社長は明山茶業ならではの価値観を見出した。</p>
        <p class="interview__txt">お茶という商品は、嗜好品です。なくてもいいし、あってもいい。<br>ただ、人がお茶を楽しむ時間を想像すると、それはきっと人生において良い場面なんです。一息つきたいそんな大切な時間に、嫌な人から買ったものや、思いのない人が作ったものを持っていきたくないじゃないですか。つまり、「誰がそれを扱っているか」が重要なんです。<br>だからこそ明山茶業は「扱う人を育てる」会社であり続けたい。お茶を愛して、人を大切にする。それが私たちの目指す姿だと思っています。</p>
